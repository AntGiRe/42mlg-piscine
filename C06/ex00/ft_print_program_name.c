#include <unistd.h>

void	ft_putchar(char c)
{
	write (1, &c, 1);
}

void	print_program_name(char **argv)
{
	char	*str;
	int		i;

	str = argv[0];
	i = 0;
	while (str[i] != '\0')
	{
		ft_putchar(str[i]);
		i++;
	}
	ft_putchar('\n');
}

int	main(int argc, char **argv)
{
	if (argc > 0)
		print_program_name(argv);
}
